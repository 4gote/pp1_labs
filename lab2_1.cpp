//data types
#include <iostream>
#include <string>
using namespace std;
 
int main () {
  int myNum = 10;               
  float myFloatNum = 3.14;     
  double myDoubleNum = 2.94;  
  char myLetter = 'A';         
  bool myBoolean = true;       
  string myString = "Hello";
  cout << "int: " << myNum << "\n";
  cout << "float: " << myFloatNum << "\n";
  cout << "double: " << myDoubleNum << "\n";
  cout << "char: " << myLetter << "\n";
  cout << "bool: " << myBoolean << "\n";
  cout << "string: " << myString << "\n";
 
  return 0;
}